def main():
    # Use the ORIGINAL file to show what will be changed
    CSV_PATH = '/Users/aaron.w/Desktop/aeo_system/AEODOC_Fixed_Paths.csv'
    df = pd.read_csv(CSV_PATH, dtype=str)

    path_map = get_path_map()
    
    print('| 行号 | 上级文件夹 | 原始名称 | 优化后名称 |')
    print('|---|---|---|---|')
    
    for idx, row in df.iterrows():
        orig = str(row['审核项目名称']).strip() if pd.notna(row['审核项目名称']) else ''
        path = str(row['文件路径']).strip() if pd.notna(row['文件路径']) else ''
        
        new_name = path_map.get((orig, path))
        
        if new_name and new_name != orig:
            path_parts = path.split('/')
            parent = path_parts[-2] if len(path_parts) >= 2 else '/'
            print(f'| {idx + 2} | {parent} | {orig} | {new_name} |')

if __name__ == "__main__":
    main()
