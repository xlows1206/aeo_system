def optimize_name(row):
    a_name = str(row['审核项目名称']).strip() if pd.notna(row.get('审核项目名称')) else ''
    c_path = str(row['文件路径']).strip() if pd.notna(row.get('文件路径')) else ''
    return path_map.get((a_name, c_path), a_name)


# 应用优化
original_names = df['审核项目名称'].copy()
df['审核项目名称'] = df.apply(optimize_name, axis=1)

# 打印变更明细
changes = []
for i in range(len(df)):
    orig = str(original_names.iloc[i]).strip()
    new = str(df.iloc[i]['审核项目名称']).strip()
    if orig != new:
        path = str(df.iloc[i]['文件路径'])
        changes.append((i + 2, orig, new, path))

print(f"共修改 {len(changes)} 行：\n")
print(f"{'行号':<5} {'原名称':<22} {'优化后名称':<25} 路径末段")
print("-" * 100)
for row_num, orig, new, path in changes:
    path_tail = path.split('/')[-1] if '/' in path else path
    print(f"{row_num:<5} {orig:<22} {new:<25} .../{path_tail}")

